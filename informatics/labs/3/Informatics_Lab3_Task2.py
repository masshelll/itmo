import re
pattern = r"([А-Я])\.(?!\1)[А-Я]\."
def test(f):
    print([s for s in f.split('\n') if re.search(pattern, s)])
test("""
Петров П.П. P0000
Анищенко А.А. P33113
Примеров Е.В. P0000
Иванов И.И. P0000
""")
test("""
Анищенко А.А. P33113
Анищенко А.А. P33113
Смирнов С.С. P1234
""")
test("""
А.А.А. P0000
Го .Л.Р. Р9407
""")
test("""
Сидоров А.Б. P1234
Новиков-Черкасов Г.Г. P9999
""")
test("""
Сидоров А.А. P8755
""")